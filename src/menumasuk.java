    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        // TODO add your handling code here:
        if(txtnopolisi.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Masukan No Polisi","Insert Data",JOptionPane.INFORMATION_MESSAGE);
            txtnopolisi.requestFocus();
        }else{
        masuk.setUsername(txtpetugas.getText());
        masuk.setNoPolisi(txtnopolisi.getText());
        masuk.setJam(txtjam.getText());
        txtkode.setText("P"+masuk.getTanggalBulan()+Integer.toString(masuk.getKode()));
        masuk.setKodeTransaksi(txtkode.getText());
        txtjammasuk.setText(masuk.getJam());
        textarea1.setEnabled(true);
        masuk.setStruk();
        
        
        textarea1.append(masuk.getStruk());
        notif1.setText("*Data telah tersimpan*");
        txttanggal.setText(masuk.getTanggal());
        
        
        btprint.setEnabled(true);
        txtjeniskendaraan.setEnabled(false);
        txtnopolisi.setEnabled(false);
        btsimpan.setEnabled(false);
        koneksi.kodetransaksi = txtkode.getText();
        koneksi.biaya = txthargaperjam.getText();
        koneksi.no_pol = txtnopolisi.getText();
        koneksi.jenis = (String)txtjeniskendaraan.getSelectedItem();
        koneksi.jam_in = txtjammasuk.getText();
        koneksi.datamenumasuk();
        bthapus.requestFocus();
        }
    }//GEN-LAST:event_btsimpanActionPerformed

