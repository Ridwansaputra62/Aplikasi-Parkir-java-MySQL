    private void txtjeniskendaraanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtjeniskendaraanActionPerformed
        // TODO add your handling code here:
        
        masuk.setJenisKendaraan((String) txtjeniskendaraan.getSelectedItem());
        masuk.getJenisKendaraan();
        masuk.setHargaKendaraan();
       txthargaperjam.setText(Integer.toString(masuk.getHargaKendaraan()));
        //txthargaperjam.setText(Integer.toString(masuk.getHargaKendaraan()));
        if (masuk.getJenisKendaraan().equals("Motor")||(masuk.getJenisKendaraan().equals("Mobil"))||(masuk.getJenisKendaraan().equals("Bus"))){
            txtnopolisi.setEnabled(true);
            btsimpan.setEnabled(true);
            txtnopolisi.requestFocus();
        }else{
            txtnopolisi.setEnabled(false);
            btsimpan.setEnabled(false);
            
            
        }
    }//GEN-LAST:event_txtjeniskendaraanActionPerformed

